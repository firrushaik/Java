import java.util.Scanner;
public class print1to5numbers {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter the total number you have to print: ");
        int num = sc.nextInt();
        for(int i=1;i<=num;i++){
            System.out.println(i);
        }

    }
}
